package com.example.waste;

class Detail {
    String id;
    String userid;
    String location;
    String complaints;
    String time;
    public Detail(String id, String userid, String location, String complaints, String time) {


    }

    public String getId() {
        return id;
    }

    public String getUserid() {
        return userid;
    }

    public String getLocation() {
        return location;
    }

    public String getComplaints() {
        return complaints;
    }

    public String getTime() {
        return time;
    }
}
